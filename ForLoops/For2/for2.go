package main

import (
	f "fmt"
)

//โค้ดตัวอย่างนี้จะแสดงเลขจาก 0 ถึง 100 โดยนับทีละ 10
/* Syntax for statement1; statement2; statement3{
	โค้ดที่จะถูกเรียกใช้ในแต่ละรอบของการวนลูป
}*/
func main() {
	//number := 0
	for i := 0; i <= 100; i += 10 {
		// i++
		f.Println(i)
	} // i = index
}

/*อธิบายโค้ด
i := 0; กำหนดค่าตัวนับลูป (i) และตั้งค่าเริ่มต้นเป็น 0
i <= 100; ดำเนินการวนลูปต่อเมื่อ i น้อยกว่าหรือเท่ากับ 100
i += 10 เพิ่มค่าตัวนับลูปทีละ 10 ในแต่ละรอบ
*/
