package main

import (
	f "fmt"
)

// Typed Constants (ค่าคงที่ที่มีประเภท)
// ค่าคงที่ที่ประกาศและกำหนดประเภทของมัน
const A int = 1

func main() {
	f.Println(A)
}

/*
 A คือค่าคงที่ที่มีประเภท int และมีค่าเป็น 1
 โดยการใช้ const A int = 1 ในการประกาศ ทำให้ A
 เป็นค่าคงที่ที่มีประเภทและไม่สามารถเปลี่ยนแปลงค่าได้
 หลังจากที่กำหนดค่าเริ่มต้น
*/
