package main

import (
	f "fmt"
)

// Untyped Constants (ค่าคงที่ที่ไม่มีประเภท)
// ค่าคงที่ที่ไม่ได้ระบุประเภทเป็นชนิดของตัวมัน
const B = 2

func main() {
	f.Println(B)
}

/*
B คือค่าคงที่ที่ไม่ระบุประเภท (untyped constant) และมีค่าเป็น 2
โดยไม่ต้องระบุประเภทแบบชัดเจน ซึ่งใน
Go, ค่าคงที่ที่ไม่ระบุประเภทจะถูกใช้ในบางกรณี
ที่ต้องการให้ระบบจัดการประเภทให้โดยอัตโนมัติ
*/
