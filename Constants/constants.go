package main

//การประกาศค่าคงที่:
//ค่าคงที่สามารถประกาศได้ทั้งภายในและภายนอกฟังก์ชัน
import (
	f "fmt"
)

// ค่าคงที่ (constants) เป็นตัวแปรที่มีค่าที่ไม่สามารถเปลี่ยนแปลงได้
// syntax
// const CONSTNAME type = value
const PI = 3.14 // คือการประกาศค่าคงที่ชื่อ PI และมีค่าเท่ากับ 3.14

func main() {
	f.Println(PI)
}

