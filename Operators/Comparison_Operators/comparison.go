package main

import (
	f "fmt"
)

//ตัวดำเนินการเปรียบเทียบ (Comparison Operators)
/* ตัวดำเนินการเปรียบเทียบใช้สำหรับเปรียบเทียบค่าสองค่า โดย
ผลลัพธ์ที่ได้จะเป็น จริง (true) หรือ เท็จ (false)
*/
//เราใช้ตัวดำเนินการมากกว่า (>) เพื่อดูว่า 5 มากกว่า 3 หรือไม่
var x = 5 //กำหนดค่าเริ่มต้นให้กับตัวแปร x เป็น 5
var y = 3 //กำหนดค่าเริ่มต้นให้กับตัวแปร y เป็น 3

func main() {
	//การใช้เครื่องหมาย > เพื่อดูว่า 5 มากกว่า 3 หรือไม่
	f.Println(x > y) // 5 มีค่ามากกว่า 3 ผลลัพธ์คือ true
}
