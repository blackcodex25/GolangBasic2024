package main

import (
	f "fmt"
)

/*
การใช้เวิร์บการจัดรูปแบบสำหรับข้อมูลชนิดบูลีน (Boolean Formatting Verbs) ในภาษา Go
มีเพียงเวิร์บเดียวคือ %t ซึ่งใช้สำหรับพิมพ์ค่าของตัวดำเนินการบูลีน
ในรูปแบบ true หรือ false ตามค่าที่กำหนด
(เหมือนกับการใช้ %v สำหรับข้อมูลที่มีชนิดเป็นบูลีน)
*/
var i = true
var j = false

// การจัดรูปแบบสำหรับข้อมูลชนิดบูลีน
func main() {
	f.Printf("%t\n", i) //true
	f.Printf("%t\n", j) // false
}
