// Go -ของการเพิ่มช่องว่างระหว่างอาร์กิวเมนต์ในฟังก์ชัน Print()
package main

import (
	f "fmt"
)

/*
ถ้าต้องการเพิ่มช่องว่างระหว่างอาร์กิวเมนต์ที่เป็นสตริง
สามารถใช้ " " (ช่องว่าง)
ระหว่างอาร์กิวเมนต์ในฟังก์ชัน Print() ได้
*/
var i, j string = "Dev", "Programing"

func main() {
	f.Print(i, " ", j)
} // การใช้ " " (ช่องว่าง) ระหว่าง i และ j เพื่อเพิ่มช่องว่างระหว่างค่าทั้งสอง
