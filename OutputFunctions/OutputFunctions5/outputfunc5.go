/*
ฟังก์ชัน Println() ใน Go

ฟังก์ชัน Println() ใน Go
ฟังก์ชัน Println() มีลักษณะคล้ายกับฟังก์ชัน Print() แต่มีความแตกต่างที่สำคัญสองประการ:

มีการเพิ่มช่องว่างระหว่างอาร์กิวเมนต์
มีการเพิ่มบรรทัดใหม่ (newline) หลังจากพิมพ์ข้อความเสร็จ
*/
package main

import (
	f "fmt"
)

var i, j string = "Hello", "Dev"

func main() {
	f.Println(i, j)
	//ฟังก์ชันนี้จะเพิ่มช่องว่างระหว่างอาร์กิวเมนต์ (i และ j) โดยอัตโนมัติ

}
