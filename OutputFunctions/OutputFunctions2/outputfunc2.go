// ฟังก์ชัน Print() ใน Go กับการขึ้นบรรทัดใหม่
// ถ้าต้องการพิมพ์ข้อความแต่ละอาร์กิวเมนต์ในบรรทัดใหม่
// สามารถใช้ตัวอักษร \n เพื่อขึ้นบรรทัดใหม่ได้
package main

import (
	f "fmt"
)

var i, j string = "Dev", "Programing"

func main() {
	f.Print(i, "\n")
	f.Print(j, "\n")
	//การใช้ \n เพื่อบอกให้ฟังก์ชัน Print ขึ้นบรรทัดใหม่หลังจากพิมพ์ค่าของ i และ j
}
