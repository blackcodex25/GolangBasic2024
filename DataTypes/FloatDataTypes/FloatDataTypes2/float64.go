package main

import (
	f "fmt"
)

// float64 เป็นประเภทข้อมูลใน Go ที่ใช้สำหรับเก็บค่าตัวเลขที่มีจุดทศนิยม
// โดยมีขนาด 64 บิตและสามารถเก็บค่าได้ตั้งแต่ -1.7e+308 ถึง +1.7e+308
var x float64 = 1.7e+308

// float64 สามารถเก็บค่าตัวเลขที่มีขนาดใหญ่และแม่นยำกว่าประเภท float32
func main() {
	f.Printf("Type: %T, value: %v", x, x)
}

/*
ตัวแปร x ถูกประกาศเป็นชนิด float64 และกำหนดค่าเป็น 1.7e+308 ซึ่งเป็นค่าในรูปแบบวิทยาศาสตร์ (Scientific Notation)
*/
